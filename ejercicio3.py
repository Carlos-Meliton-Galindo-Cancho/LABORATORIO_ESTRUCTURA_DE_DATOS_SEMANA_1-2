"""
EJERCICIO 3   

ÁREA DE UN TRIÁNGULO:

Pide la base y la altura de un triángulo al usuario y calcula su área.

"""


base=int(input("ingresa la base del triangulo: "))      # solicita al usuario que ingrese la base del triángulo, luego lo convierte a un entero y lo almacena a la variable base
altura=int(input("ingresa la altura del triangulo: "))  # solicita al usuario que ingrese la altura del triángulo, luego lo convierte a un entero y lo almacena a la variable altura

area= (base * altura)/2                    # aquí se calcula el área del triángulo utilizando la fórmula del área y el resultado se guarda en la variable area
print(f" el area del triangulo es: {area}")   # finalmente, se imprime el valor del área del triángulo utilizando una f-string para formatear la cadena de salida















